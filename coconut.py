def main():
    """The Main Coconut Entry Point."""
    try:
        compiler.readline
    except AttributeError:
        cmd = compiler.cli()
    else:
        cmd = compiler.cli(color="cyan")
    cmd.start()

if __name__ == "__main__":
    main()
